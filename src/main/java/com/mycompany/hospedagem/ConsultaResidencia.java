    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada >= 0) {
            
            String codigo = (String) jTable1.getValueAt(linhaSelecionada, 0);
            String rua = (String) jTable1.getValueAt(linhaSelecionada, 1);
            int numero = (int) jTable1.getValueAt(linhaSelecionada, 2);
            String bairro = (String) jTable1.getValueAt(linhaSelecionada, 3);
            String cep = (String) jTable1.getValueAt(linhaSelecionada, 4);
            String telefone = (String) jTable1.getValueAt(linhaSelecionada, 5);
            String email = (String) jTable1.getValueAt(linhaSelecionada, 6);
            int quartos = (int) jTable1.getValueAt(linhaSelecionada, 7);

            ResidenciaTela cadastro = new ResidenciaTela(true, new Residencia(codigo, rua, numero, bairro, cep, telefone, email, quartos));
            cadastro.setVisible(true);
            jButton5ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada >= 0) {
            String codigo = (String) jTable1.getValueAt(linhaSelecionada, 0);
            ResidenciaDao residencia = new ResidenciaDao();
            residencia.deletarResidencia(codigo);
            jButton5ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

