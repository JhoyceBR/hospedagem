    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ResidenciaDao residencia = new ResidenciaDao();
        for (Residencia r : residencia.listarResidencias()) {
            modelo.addRow(new Object[]{r.getCodResidencia(), r.getRua(), r.getNumero(), r.getBairro(), r.getCep(), r.getTelefone(), r.getEmail(), r.getQuartos()});
        }
    }//GEN-LAST:event_jButton5ActionPerformed

