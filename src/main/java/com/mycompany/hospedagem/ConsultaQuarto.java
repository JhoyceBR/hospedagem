    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada >= 0) {

            String codigo = (String) jTable1.getValueAt(linhaSelecionada, 0);
            String codigoresidencia = (String) jTable1.getValueAt(linhaSelecionada, 1);
            double diaria = (double) jTable1.getValueAt(linhaSelecionada, 2);
            int numeroMaximo = (int) jTable1.getValueAt(linhaSelecionada, 3);
            boolean arCondicionado = (boolean) jTable1.getValueAt(linhaSelecionada, 4);
            boolean hidromassagem = (boolean) jTable1.getValueAt(linhaSelecionada, 5);
            

            QuartoTela cadastro = new QuartoTela(true, new Quarto(codigo, codigoresidencia, diaria, numeroMaximo, arCondicionado, hidromassagem));
            cadastro.setVisible(true);
            jButton5ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada >= 0) {
            String codigo = (String) jTable1.getValueAt(linhaSelecionada, 0);
            QuartoDao quarto = new QuartoDao();
            quarto.deletarQuarto(codigo);
            jButton5ActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

