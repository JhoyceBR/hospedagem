    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        
        QuartoDao quarto = new QuartoDao();
        
        
        for (Quarto q : quarto.listarQuartos(codigoResidencia.getText())) {
            modelo.addRow(new Object[]{q.getID(), q.getCodResidencia(), q.getValorDiaria(), q.getNumeroMaxHospede(), q.getArCondicionado(), q.getHidromassagem()});
        }
    }//GEN-LAST:event_jButton5ActionPerformed

